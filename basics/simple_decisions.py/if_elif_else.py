u_input = input("Towards which direction should I paint (up, down, left or right)? ")


if (u_input == "up"):
  print("I am painting in the upward direction!")
elif (u_input == "down"):
  print("I am painting in the downwards direction!")
elif (u_input == "left"):
  print("I am painting in the left direction!")
elif (u_input == "right"):
  print("I am painting in the right direction!")

else: 
  print("I am not painting in that direction ")
  